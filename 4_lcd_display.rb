NUMBERS = {
  1 => ['   ', '  |', '  |'],
  2 => [' _ ', ' _|', '|_ '],
  3 => [' _ ', ' _|', ' _|'],
  4 => ['   ', '|_|', '  |'],
  5 => [' _ ', '|_ ', ' _|'],
  6 => [' _ ', '|_ ', '|_|'],
  7 => [' _ ', '  |', '  |'],
  8 => [' _ ', '|_|', '|_|'],
  9 => [' _ ', '|_|', ' _|'],
  0 => [' _ ', '| |', '|_|']
}.freeze

number = ARGV[0] ? ARGV[0] : '0'

3.times do |line|
  number.each_char do |num|
    print "#{NUMBERS[num.to_i][line]} "
  end
  puts
end
